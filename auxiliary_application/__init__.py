##
#@package auxiliary_application
#@brief This package delivers functions for generating a pair of RSA (4096) keys. The private key is encrypted using AES cipher with 256 bit hash from 8 digit PIN given by the user.
#
#The `auxiliary_application` package contains the following modules:
#- `main.py` - Handles the main application logic.
#- `gui.py` - Provides the graphical user interface.
#- `keygen.py` - Responsible for RSA key generation and encryption.
#